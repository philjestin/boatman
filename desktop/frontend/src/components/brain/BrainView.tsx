// Brain Index
// =============================================================================

interface BrainIndexProps {
  brains: BrainEntry[];
  selectedBrain: BrainDetail | null;
  validation: import('../../types').BrainValidationResult | null;
  onSelect: (brain: BrainEntry) => void;
  onValidate: (id: string) => void;
}

function BrainIndex({ brains, selectedBrain, validation, onSelect, onValidate }: BrainIndexProps) {
  if (brains.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center h-full text-slate-500 p-8">
        <Brain className="w-12 h-12 mb-3 opacity-30" />
        <p className="text-sm font-medium mb-1">No brains found</p>
        <p className="text-xs text-center">
          Create brain YAML files in <code className="text-purple-400">.boatman/brains/</code> to provide
          domain knowledge to agents.
        </p>
      </div>
    );
  }

  return (
    <div className="divide-y divide-slate-800">
      {brains.map((brain) => {
        const isSelected = selectedBrain?.id === brain.id;
        return (
          <div key={brain.id}>
            {/* Brain header */}
            <button
              onClick={() => onSelect(brain)}
              className="w-full flex items-start gap-3 px-4 py-3 hover:bg-slate-800/50 transition-colors text-left"
            >
              <div className="mt-0.5">
                {isSelected ? (
                  <ChevronDown className="w-4 h-4 text-purple-400" />
                ) : (
                  <ChevronRight className="w-4 h-4 text-slate-500" />
                )}
              </div>
              <div className="flex-1 min-w-0">
                <div className="flex items-center gap-2">
                  <span className="text-sm font-medium text-slate-200">{brain.name}</span>
                  <ConfidenceBadge confidence={brain.confidence} />
                </div>
                <p className="text-xs text-slate-400 mt-0.5 line-clamp-2">{brain.description}</p>
                <div className="flex flex-wrap gap-1 mt-1.5">
                  {brain.keywords.slice(0, 5).map((kw) => (
                    <span
                      key={kw}
                      className="px-1.5 py-0.5 text-xs bg-purple-900/30 text-purple-300 rounded"
                    >
                      {kw}
                    </span>
                  ))}
                  {brain.entities.slice(0, 3).map((ent) => (
                    <span
                      key={ent}
                      className="px-1.5 py-0.5 text-xs bg-blue-900/30 text-blue-300 rounded"
                    >
                      {ent}
                    </span>
                  ))}
                  {brain.keywords.length + brain.entities.length > 8 && (
                    <span className="text-xs text-slate-500">
                      +{brain.keywords.length + brain.entities.length - 8} more
                    </span>
                  )}
                </div>
              </div>
            </button>

            {/* Expanded detail */}
            {isSelected && selectedBrain && (
              <BrainDetailView
                brain={selectedBrain}
                validation={validation}
                onValidate={() => onValidate(brain.id)}
              />
            )}
          </div>
        );
      })}
    </div>
  );
}

