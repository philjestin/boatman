interface BrainDetailViewProps {
  brain: BrainDetail;
  validation: import('../../types').BrainValidationResult | null;
  onValidate: () => void;
}

function BrainDetailView({ brain, validation, onValidate }: BrainDetailViewProps) {
  const [expandedSections, setExpandedSections] = useState<Set<string>>(
    new Set(brain.sections.map((s) => s.title))
  );

  const toggleSection = (title: string) => {
    setExpandedSections((prev) => {
      const next = new Set(prev);
      if (next.has(title)) next.delete(title);
      else next.add(title);
      return next;
    });
  };

  return (
    <div className="bg-slate-850 border-t border-slate-800">
      {/* Meta row */}
      <div className="flex items-center gap-4 px-4 py-2 bg-slate-800/50 text-xs text-slate-400">
        <span>v{brain.version}</span>
        {brain.lastUpdated && <span>Updated: {brain.lastUpdated}</span>}
        {brain.filePatterns.length > 0 && (
          <span className="flex items-center gap-1">
            <FolderOpen className="w-3 h-3" />
            {brain.filePatterns.join(', ')}
          </span>
        )}
        <button
          onClick={onValidate}
          className="ml-auto text-xs text-purple-400 hover:text-purple-300 transition-colors"
        >
          Validate
        </button>
      </div>

      {/* Validation result */}
      {validation && (
        <div className={`px-4 py-2 text-xs border-t border-slate-800 ${
          validation.valid ? 'bg-green-900/20' : 'bg-red-900/20'
        }`}>
          <div className="flex items-center gap-1.5">
            {validation.valid ? (
              <><CheckCircle className="w-3.5 h-3.5 text-green-400" /> <span className="text-green-400">Valid</span></>
            ) : (
              <><AlertTriangle className="w-3.5 h-3.5 text-red-400" /> <span className="text-red-400">{validation.errors.length} error(s)</span></>
            )}
          </div>
          {validation.errors.map((err, i) => (
            <div key={i} className="text-red-300 mt-1 pl-5">- {err}</div>
          ))}
          {validation.stale.length > 0 && (
            <div className="mt-1.5">
              <span className="text-amber-400">Stale references:</span>
              {validation.stale.map((s, i) => (
                <div key={i} className="text-amber-300 pl-5">- {s.path} ({s.reason})</div>
              ))}
            </div>
          )}
        </div>
      )}

      {/* Sections */}
      <div className="divide-y divide-slate-800">
        {brain.sections.map((section) => {
          const isExpanded = expandedSections.has(section.title);
          return (
            <div key={section.title}>
              <button
                onClick={() => toggleSection(section.title)}
                className="w-full flex items-center gap-2 px-4 py-2 hover:bg-slate-800/30 transition-colors text-left"
              >
                {isExpanded ? (
                  <ChevronDown className="w-3.5 h-3.5 text-slate-400" />
                ) : (
                  <ChevronRight className="w-3.5 h-3.5 text-slate-400" />
                )}
                <FileText className="w-3.5 h-3.5 text-purple-400" />
                <span className="text-xs font-medium text-slate-300">{section.title}</span>
              </button>
              {isExpanded && (
                <div className="px-10 pb-3 text-xs text-slate-300 whitespace-pre-wrap leading-relaxed">
                  {section.content}
                </div>
              )}
            </div>
          );
        })}
      </div>

      {/* References */}
      {brain.references.length > 0 && (
        <div className="border-t border-slate-800 px-4 py-2">
          <div className="flex items-center gap-1.5 mb-1.5">
            <Tag className="w-3 h-3 text-slate-400" />
            <span className="text-xs font-medium text-slate-400">References</span>
          </div>
          <div className="space-y-1">
            {brain.references.map((ref) => (
              <div key={ref.path} className="flex items-center gap-2 text-xs">
                <code className="text-cyan-400 font-mono">{ref.path}</code>
                <span className="text-slate-500">- {ref.description}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}

