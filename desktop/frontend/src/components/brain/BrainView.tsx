function ConfidenceBadge({ confidence }: { confidence: number }) {
  const pct = Math.round(confidence * 100);
  const color =
    pct >= 80 ? 'bg-green-900/30 text-green-400' :
    pct >= 50 ? 'bg-amber-900/30 text-amber-400' :
    'bg-red-900/30 text-red-400';

  return (
    <span className={`px-1.5 py-0.5 text-xs rounded ${color}`}>
      {pct}%
    </span>
  );
}

function SignalTypeBadge({ type }: { type: string }) {
  const config: Record<string, { label: string; color: string }> = {
    review_failure: { label: 'Review', color: 'bg-red-900/30 text-red-400' },
    refactor_loop: { label: 'Refactor', color: 'bg-orange-900/30 text-orange-400' },
    repeated_file_read: { label: 'File Read', color: 'bg-blue-900/30 text-blue-400' },
    user_correction: { label: 'Correction', color: 'bg-purple-900/30 text-purple-400' },
    error_retry_loop: { label: 'Retry', color: 'bg-amber-900/30 text-amber-400' },
  };

  const cfg = config[type] || { label: type, color: 'bg-slate-700 text-slate-400' };

  return (
    <span className={`px-1.5 py-0.5 text-xs rounded flex-shrink-0 ${cfg.color}`}>
      {cfg.label}
    </span>
  );
}
