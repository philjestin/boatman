interface SignalListProps {
  signals: SignalEntry[];
}

function SignalList({ signals }: SignalListProps) {
  if (signals.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center h-full text-slate-500 p-8">
        <Zap className="w-12 h-12 mb-3 opacity-30" />
        <p className="text-sm font-medium mb-1">No signals detected</p>
        <p className="text-xs text-center">
          Signals are collected automatically during agent workflows
          when knowledge gaps are detected.
        </p>
      </div>
    );
  }

  // Group by domain
  const grouped = signals.reduce<Record<string, SignalEntry[]>>((acc, sig) => {
    if (!acc[sig.domain]) acc[sig.domain] = [];
    acc[sig.domain].push(sig);
    return acc;
  }, {});

  return (
    <div className="divide-y divide-slate-800">
      {Object.entries(grouped).map(([domain, domainSignals]) => (
        <div key={domain} className="px-4 py-3">
          <div className="flex items-center gap-2 mb-2">
            <span className="text-sm font-medium text-slate-200 capitalize">{domain}</span>
            <span className="px-1.5 py-0.5 text-xs bg-amber-900/50 text-amber-400 rounded">
              {domainSignals.length}
            </span>
          </div>
          <div className="space-y-2">
            {domainSignals.map((sig, i) => (
              <div key={i} className="flex items-start gap-2 text-xs">
                <SignalTypeBadge type={sig.type} />
                <div className="flex-1 min-w-0">
                  <p className="text-slate-300 truncate">{sig.details || 'No details'}</p>
                  <div className="flex items-center gap-3 mt-0.5 text-slate-500">
                    <span>x{sig.count}</span>
                    <span>Last: {new Date(sig.lastSeen).toLocaleDateString()}</span>
                    {sig.filePaths.length > 0 && (
                      <span className="truncate">{sig.filePaths[0]}</span>
                    )}
                  </div>
                </div>
              </div>
            ))}
          </div>
        </div>
      ))}
    </div>
  );
}

