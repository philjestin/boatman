import { useState, useEffect } from 'react';
import {
  Brain,
  ChevronRight,
  ChevronDown,
  AlertTriangle,
  CheckCircle,
  FileText,
  Tag,
  Search,
  Zap,
  FolderOpen,
  RefreshCw,
  Sparkles,
  X,
} from 'lucide-react';
import { useBrain } from '../../hooks/useBrain';
import type { BrainEntry, BrainDetail, SignalEntry } from '../../types';

type BrainTab = 'index' | 'signals';

export function BrainView() {
  const {
    brains,
    selectedBrain,
    signals,
    brainDirs,
    validation,
    autoGenerated,
    isLoading,
    loadBrains,
    loadBrain,
    validateBrain,
    loadSignals,
    loadBrainDirs,
    setSelectedBrain,
    autoDistill,
    dismissAutoGenerated,
  } = useBrain();

  const [activeTab, setActiveTab] = useState<BrainTab>('index');
  const [searchQuery, setSearchQuery] = useState('');

  useEffect(() => {
    loadBrains();
    loadBrainDirs();
  }, [loadBrains, loadBrainDirs]);

  const filteredBrains = brains.filter((b) => {
    if (!searchQuery) return true;
    const q = searchQuery.toLowerCase();
    return (
      b.name.toLowerCase().includes(q) ||
      b.description.toLowerCase().includes(q) ||
      b.keywords.some((k) => k.toLowerCase().includes(q)) ||
      b.entities.some((e) => e.toLowerCase().includes(q))
    );
  });

  const handleSelectBrain = async (brain: BrainEntry) => {
    if (selectedBrain?.id === brain.id) {
      setSelectedBrain(null);
      return;
    }
    await loadBrain(brain.id);
  };

  const handleValidate = async (id: string) => {
    await validateBrain(id);
  };

  const handleLoadSignals = async () => {
    await loadSignals();
    setActiveTab('signals');
  };

  return (
    <div className="flex flex-col h-full bg-slate-900">
      {/* Header */}
      <div className="flex items-center justify-between px-4 py-3 border-b border-slate-700">
        <div className="flex items-center gap-2">
          <Brain className="w-5 h-5 text-purple-400" />
          <h2 className="text-sm font-semibold text-slate-200">Domain Knowledge</h2>
          <span className="px-1.5 py-0.5 text-xs bg-slate-700 rounded-full text-slate-400">
            {brains.length}
          </span>
        </div>
        <div className="flex items-center gap-2">
          <button
            onClick={autoDistill}
            className="p-1.5 text-slate-400 hover:text-purple-300 transition-colors"
            title="Auto-distill signals into brains"
          >
            <Sparkles className="w-4 h-4" />
          </button>
          <button
            onClick={() => { loadBrains(); loadBrainDirs(); }}
            className="p-1.5 text-slate-400 hover:text-slate-200 transition-colors"
            title="Refresh"
          >
            <RefreshCw className={`w-4 h-4 ${isLoading ? 'animate-spin' : ''}`} />
          </button>
        </div>
      </div>

      {/* Auto-generated notification */}
      {autoGenerated.length > 0 && (
        <div className="flex items-center gap-2 px-4 py-2 bg-purple-900/30 border-b border-purple-800/50 text-xs">
          <Sparkles className="w-3.5 h-3.5 text-purple-400 flex-shrink-0" />
          <span className="text-purple-200 flex-1">
            {autoGenerated.length} brain{autoGenerated.length > 1 ? 's' : ''} auto-generated from signals:{' '}
            {autoGenerated.map((r) => r.domain).join(', ')}
          </span>
          <button
            onClick={dismissAutoGenerated}
            className="p-0.5 text-purple-400 hover:text-purple-200 transition-colors"
          >
            <X className="w-3.5 h-3.5" />
          </button>
        </div>
      )}

      {/* Tabs */}
      <div className="flex border-b border-slate-700">
        <button
          onClick={() => setActiveTab('index')}
          className={`flex items-center gap-1.5 px-4 py-2 text-xs transition-colors border-b-2 ${
            activeTab === 'index'
              ? 'border-purple-500 text-slate-100'
              : 'border-transparent text-slate-400 hover:text-slate-200'
          }`}
        >
          <Brain className="w-3.5 h-3.5" />
          Brains
        </button>
        <button
          onClick={handleLoadSignals}
          className={`flex items-center gap-1.5 px-4 py-2 text-xs transition-colors border-b-2 ${
            activeTab === 'signals'
              ? 'border-amber-500 text-slate-100'
              : 'border-transparent text-slate-400 hover:text-slate-200'
          }`}
        >
          <Zap className="w-3.5 h-3.5" />
          Signals
          {signals.length > 0 && (
            <span className="px-1 py-0.5 text-xs bg-amber-900/50 text-amber-400 rounded">
              {signals.length}
            </span>
          )}
        </button>
      </div>

      {/* Search (brains tab only) */}
      {activeTab === 'index' && (
        <div className="px-3 py-2 border-b border-slate-800">
          <div className="relative">
            <Search className="absolute left-2.5 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-slate-500" />
            <input
              type="text"
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              placeholder="Search brains..."
              className="w-full pl-8 pr-3 py-1.5 text-xs bg-slate-800 border border-slate-700 rounded text-slate-200 placeholder-slate-500 focus:outline-none focus:border-purple-500"
            />
          </div>
        </div>
      )}

      {/* Content */}
      <div className="flex-1 overflow-y-auto">
        {activeTab === 'index' && (
          <BrainIndex
            brains={filteredBrains}
            selectedBrain={selectedBrain}
            validation={validation}
            onSelect={handleSelectBrain}
            onValidate={handleValidate}
          />
        )}
        {activeTab === 'signals' && <SignalList signals={signals} />}
      </div>

      {/* Brain directories footer */}
      {brainDirs.length > 0 && (
        <div className="px-3 py-2 border-t border-slate-800 text-xs text-slate-500">
          <div className="flex items-center gap-1">
            <FolderOpen className="w-3 h-3" />
            <span>Scanning: {brainDirs.map((d) => d.split('/').slice(-2).join('/')).join(', ')}</span>
          </div>
        </div>
      )}
    </div>
  );
}

